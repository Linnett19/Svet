    private static Style applyCyclicGradient(Style baseStyle, int[] colors, int cycleTime) {
        long time = System.currentTimeMillis() % cycleTime;
        float t = (float) time / cycleTime * colors.length;
        int index = (int) t;
        float blend = t - index;
        int nextIndex = (index + 1) % colors.length;
        int color = lerpColor(colors[index], colors[nextIndex], (float) (0.5 * (1 - Math.cos(blend * Math.PI))));

        return baseStyle
                .withColor(color)
                .withItalic(false);
    }

    private static int lerpColor(int color1, int color2, float t) {
        int r1 = (color1 >> 16) & 0xFF, g1 = (color1 >> 8) & 0xFF, b1 = color1 & 0xFF;
        int r2 = (color2 >> 16) & 0xFF, g2 = (color2 >> 8) & 0xFF, b2 = color2 & 0xFF;
        int r = (int) (r1 + (r2 - r1) * t);
        int g = (int) (g1 + (g2 - g1) * t);
        int b = (int) (b1 + (b2 - b1) * t);
        return (r << 16) | (g << 8) | b;
    }


